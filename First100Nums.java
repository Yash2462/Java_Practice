public class First100Nums {

    public static void main(String[] args) {
        // using for Loop
        for (int i = 0; i <100 ; i++) {
            System.out.println(i);
        }
        // using While Loop
        int i = 0;
        while (i < 100){
            System.out.println(i);
            i++;
        }
        // using Do While Loop
        int j = 0;
        do {
            System.out.println(j);
            j++;
        }while (j <100);
    }
}
